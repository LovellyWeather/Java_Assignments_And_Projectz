public class Node {

    int data;
    Node leftChild;
    Node rightChild;

    public Node(int data) {

        this.data = data;

    }

    public int getData() {

        return data;

    }

    public Node getLeftChild() {

        return leftChild;

    }

    public Node getRightChild() {

        return rightChild;

    }

}
